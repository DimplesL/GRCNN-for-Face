def combined_loss(embedding, labels, out_num, weights, scale_factor=64., m1=0.2, m2=0.2):
    cos_m = tf.constant(math.cos(m1))
    sin_m = tf.constant(math.sin(m1))
    mm = math.sin(m1)*m1 
    threshold = math.cos(math.pi - m1)
    with tf.variable_scope('combined_loss'):
        # cos(theta+m)
      cos_t = tf.matmul(embedding, weights, name='cos_t')
      cos_t2 = tf.square(cos_t, name='cos_2')
      sin_t2 = tf.subtract(1., cos_t2, name='sin_2')
      sin_t = tf.sqrt(sin_t2, name='sin_t')
      cos_mt = scale_factor * tf.subtract(tf.multiply(cos_t, cos_m), tf.multiply(sin_t, sin_m), name='cos_mt')

      cond_v = cos_t - threshold
      cond = tf.cast(tf.nn.relu(cond_v, name='if_else'), dtype=tf.bool)
 
      keep_val = scale_factor * (cos_t - mm)

      cos_mt_temp = tf.where(cond, cos_mt, keep_val)
      cos_mt_temp1 = tf.subtract(cos_mt_temp, scale_factor*m2)

      labels = tf.cast(labels, tf.int32)     
      mask = tf.one_hot(labels, depth=out_num, name='one_hot_mask')

      inv_mask = tf.subtract(1.0, mask, name='inverse_mask')
      s_cos_t = tf.multiply(scale_factor, cos_t, name='scalar_cos_t')
      output = tf.add(tf.multiply(s_cos_t, inv_mask), tf.multiply(cos_mt_temp1, mask), name='combined_loss_output')
    return output


def get_l2_norm_emb(emb):
    emb_norm = np.linalg.norm(emb, ord=2, axis=0)
    emb_ret = np.zeros(len(emb))
    emb_new = np.divide(emb, emb_norm)
    for i in xrange(len(emb_new)):
        emb_ret[i] = emb_new[i]
    return emb_ret


def distance(embeddings1, embeddings2, distance_metric=0):
    if distance_metric==0:
        # Euclidian distance
        diff = np.subtract(embeddings1, embeddings2)
        dist = np.sum(np.square(diff),1)
    elif distance_metric==1:
        # Distance based on cosine similarity
        dot = np.sum(np.multiply(embeddings1, embeddings2), axis=1)
        norm = np.linalg.norm(embeddings1, axis=1) * np.linalg.norm(embeddings2, axis=1)
        similarity = dot / norm
        dist = np.arccos(similarity) / math.pi
    else:
        raise 'Undefined distance metric %d' % distance_metric 
        
    return dist


